#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Правки VKR_complete.md:
  1. Введение ненумерованное: ## 1. Введение → ## Введение
  2. Заголовок документа: # ВКР магистра → # Магистерская диссертация
  3. Все упоминания «ВКР» в тексте → нейтральные замены
"""
import re
from pathlib import Path

PATH = Path("VKR_complete.md")
text = PATH.read_text(encoding="utf-8")

# ── 1. Ненумерованное Введение ────────────────────────────────────────────
text = text.replace("## 1. Введение", "## Введение")

# ── 2. Заголовок документа ───────────────────────────────────────────────
text = text.replace("# ВКР магистра", "# Магистерская диссертация")

# ── 3. Замены «ВКР» → варианты по контексту ─────────────────────────────
# Порядок важен: более длинные фразы — первыми.

replacements = [
    # заголовок работы в теме (полная строка)
    ("Тема: «Разработка системы детекции",
     "Тема: «Разработка системы детекции"),  # без изменений

    # таблицы — заголовки столбцов
    ("Почему это важно для ВКР",         "Значение для данного исследования"),
    ("Что сравнивается в ВКР",           "Что исследуется в работе"),

    # контекстные фразы (длинные сначала)
    ("влияют на постановку задачи в ВКР","влияют на постановку задачи в данном исследовании"),
    ("для задач ВКР важнее",             "для данного исследования важнее"),
    ("для целей ВКР важно",              "в данном исследовании важно"),
    ("Для целей ВКР важно",              "В данном исследовании важно"),
    ("одинаково релевантны и для ВКР, и для практики",
     "одинаково релевантны как для данного исследования, так и для практики"),
    ("для данной работы состоит в следующем",
     "для данной работы состоит в следующем"),  # без изменений — уже ок

    # одиночные «ВКР» в разных падежах
    ("для ВКР важен",     "для данной работы важен"),
    ("для ВКР важно",     "для данной работы важно"),
    ("Для ВКР важно",     "Для данной работы важно"),
    ("Для ВКР это",       "Для данного исследования это"),
    ("для ВКР это",       "для данного исследования это"),
    ("В рамках ВКР",      "В рамках данного исследования"),
    ("в рамках ВКР",      "в рамках данного исследования"),
    ("в контексте ВКР",   "в контексте данной работы"),
    ("В контексте ВКР",   "В контексте данной работы"),
    ("достаточная для ВКР", "достаточная для данной работы"),
    ("важны для ВКР",     "важны для данной работы"),
    ("постановку задачи в ВКР", "постановку задачи данного исследования"),
    # оставшиеся одиночные
    (" ВКР ",  " данного исследования "),
    (" ВКР,",  " данного исследования,"),
    (" ВКР.",  " данного исследования."),
    (" ВКР:",  " данного исследования:"),
    ("(ВКР)",  "(магистерской диссертации)"),
]

for old, new in replacements:
    text = text.replace(old, new)

PATH.write_text(text, encoding="utf-8")

# ── проверка ─────────────────────────────────────────────────────────────
remaining = [(i+1, l.strip()) for i, l in enumerate(text.splitlines())
             if "ВКР" in l and not l.strip().startswith("#")]
print(f"Сохранено: {PATH}  ({PATH.stat().st_size//1024} KB)")
print(f"Вхождений 'ВКР' в тексте (не заголовки): {len(remaining)}")
for ln, l in remaining:
    print(f"  стр.{ln}: {l[:90]}")
