def move_table_captions(text: str) -> str:
    lines = text.split("\n")
    out = []
    i = 0
    while i < len(lines):
        line = lines[i]
        # Строка вида «Таблица X.Y — ...» и не является строкой таблицы
        if re.match(r"^Таблица\s+\d", line) and "|" not in line:
            caption = line
            # Пропускаем пустые строки
            j = i + 1
            while j < len(lines) and lines[j].strip() == "":
                j += 1
            # Если дальше идёт таблица (строка начинается с |)
            if j < len(lines) and lines[j].lstrip().startswith("|"):
                table_rows = []
                while j < len(lines) and lines[j].lstrip().startswith("|"):
                    table_rows.append(lines[j])
                    j += 1
                # Выводим: таблица, пустая строка, название
                out.extend(table_rows)
                out.append("")
                out.append(caption)
                i = j
                continue
        out.append(line)
        i += 1
    return "\n".join(out)


