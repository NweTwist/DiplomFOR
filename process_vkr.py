# Шаг 2: удаление инлайн-форматирования (кроме код-блоков)
# ─────────────────────────────────────────────────────────────────────────────

def remove_inline_formatting(text: str) -> str:
    # Разбиваем на части: код-блоки и обычный текст
    parts = re.split(r"(```[\s\S]*?```)", text)
    result = []
    for idx, part in enumerate(parts):
        if idx % 2 == 1:          # код-блок — не трогаем
            result.append(part)
        else:
            part = re.sub(r"\*\*([^*\n]+)\*\*", r"\1", part)   # **жирный**
            part = re.sub(r"`([^`\n]+)`",         r"\1", part)  # `код`
            result.append(part)
    return "".join(result)


